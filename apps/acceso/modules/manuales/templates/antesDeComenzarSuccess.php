<p style="text-align: justify;" ><b>¿Qué es el SIGLA?</b></p>

<p style="text-align: justify;" >Llamado <b >Sistema Integral de Gestión para la Gestión Laboral, Administrativa y de Servicios</b>, es una plataforma tecnológica
que tiene como objetivo principal automatizar, unificar y gestionar los
procesos de una institución mediante aplicaciones web.</p>
<hr>
<p style="text-align: justify;" ><b>¿Quiénes tienen acceso al SIGLA?</b></p>

<p style="text-align: justify;" >La filosofía de este sistema es que
todos los trabajadores tengan acceso al mismo, ya que cada uno ejecuta tareas
directa o indirectamente dentro de la institución indiferentemente de su nivel
jerárquico. </p>

<p style="text-align: justify;" >Miremos los siguientes ejemplos:</p>

<p style="text-align: justify;" >1.  
Un trabajador de alta jerarquía ingresa al sistema
para visualizar estadísticas y tomar una decisión sobre un caso, al mismo
tiempo que uno de menor jerarquía ingresa para registrar los datos que permiten
generar las estadísticas. </p>



<p style="text-align: justify;" >2.  

Los trabajadores que ejecuten tareas netamente
manuales, pueden ingresar al SIGLA para descargar su constancia de trabajo o su
recibo de pago o para solicitar otro proceso de la Oficina de Recursos Humanos.</p>

<hr>
<p style="text-align: justify;" ><b>¿Qué tipo de sistema es el SIGLA?
¿Es un sistema administrativo, estadístico o de recursos humanos?</b></p>

<p style="text-align: justify;" >Es una plataforma diseñada para
albergar, simplificar y enlazar los diferentes sistemas de una institución,
pudiendo los mismos ser enteramente parte del SIGLA o ser sistemas aislados con
los que el SIGLA se comunica para obtener o enviar información.</p>


<hr>
<p style="text-align: justify;" ><b>¿Cuál es la ventaja de obtener o
enviar información a otros sistemas?</b></p>

<p style="text-align: justify;" >Actualmente los sistemas que maneja
una institución indiferentemente de su origen (desarrollos internos o
externos), se encuentran en su mayoría aislados unos de otros, de los cuales
hacen uso trabajadores directa o indirectamente; Retomemos el ejemplo de
recursos humanos, existe un sistema que maneja la nómina donde se procesan los
salarios, el personal activo, los reportes de pago, etc. Este sistema es
manejado únicamente por el personal de encargado del mismo, sin embargo tienen
que entregar constancias de trabajo u otros documentos al personal que labora
en la institución, es allí donde entra el SIGLA para automatizar este proceso
encargándose de obtener del sistema de recursos humanos solo los datos
necesarios para generar estos documentos de forma automática, pudiendo ser
descargado por el solicitante sin necesidad de solicitarlo al personal de
recursos humanos. De igual manera podría realizarse una herramienta que lea los
mismos datos para generar el carnet de identificación del trabajador y sea
accesible por el personal de seguridad o departamento encargado del proceso. Es
por ello que el SIGLA permite enlazar múltiples sistemas, dándonos la
posibilidad de erradicar la tramitación de información de forma manual, quédese
entendido que el ejemplo se aplicaría de forma parecida para otros sistemas
como de administración, presupuesto, inventario, etc.</p>


<hr>
<p style="text-align: justify;" ><b>¿Cuál es la principal herramienta
del SIGLA?</b></p>

<p style="text-align: justify;" >Aunque esta plataforma permite comunicarse
con otros sistemas y hasta gestionarlos tiene su propia herramienta, siendo
esta el <b >Sistema de Correspondencia</b>
para el registro, visualización y seguimiento de uno o varios documentos o
pasos de un flujo de trabajo, esto con el objeto de automatizar las
comunicaciones escritas y sus procedimientos derivados, permitiendo la
disminución de tiempos resultado del envió manual y de procesamiento, así como
también de costos por la disminución del uso de los recursos como papel, toners de impresoras y fotocopias entre otros.</p>
