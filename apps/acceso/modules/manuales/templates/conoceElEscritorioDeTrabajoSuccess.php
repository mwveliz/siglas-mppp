<p style="text-align: justify;"> <b >Conoce el escritorio de trabajo del SIGLA</b></p>

<p style="text-align: justify;" >El escritorio está compuesto por
tres zonas las cuales contienen vínculos e información que nos permitirá
navegar a través del sistema, estas zonas son: barra de inicio, menú de herramientas
y área de contenido, las cuales se distribuyen como se muestra a continuación:</p>


<p align="center" style="text-align: center;" >
<img width="350" src="/images/manuales/image003.jpg"/></p>
<hr>


<a name="area_contenido"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Área de Contenido</b></p>

<p style="text-align: justify;" >Esta zona
será utilizada como lugar genérico donde se mostrará toda la información,
vínculos, imágenes, menús, etc. de la aplicación que estemos utilizando al
momento. Inicialmente esta pantalla muestra la información del usuario
autenticado (datos básicos, datos laborales y datos de usuario).</p>

<hr>
<a name="barra_inicio"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Barra de Inicio</b></p>

<p style="text-align: justify;" >En ella encontraremos las opciones
básicas del SIGLA, las cuales <span class="GramE">son <b >Inicio</b>, <b >Salir</b>, <b >Temas</b> y <b >Contraseña</b>, además del nombre del usuario que esta autenticado en
el momento.</p>

<p align="center" style="text-align: center;" ><img width="520" src="/images/manuales/image005.jpg"/></p>

<p style="text-align: justify;" >De izquierda a derecha la barra
muestra los siguientes vínculos e información.</p>

<p style="margin-left: 36pt; text-align: justify;" ><img height="22" width="27" alt="Descripción: Descripción: Descripción: inicio" src="/images/manuales/image007.jpg"/> <b >Inicio</b>:
nos permite ir a la pantalla inicial del sistema directamente desde cualquier
modulo en donde nos encontremos.</p>

<p style="margin-left: 36pt; text-align: justify;" ><img height="23" width="23" v:shapes="Imagen_x0020_4" alt="Descripción: Descripción: Descripción: cerrarsession" src="/images/manuales/image008.jpg"/> <b >Salir</b>:
como su nombre lo indica es un vínculo para salir del sistema completamente
cerrando la sesión y llevándonos a la pantalla de autenticación o <span class="SpellE">logeo.</p>

<p style="margin-left: 36pt; text-align: justify;" ><img height="22" width="23" v:shapes="Imagen_x0020_5" alt="Descripción: Descripción: Descripción: user_m" src="/images/manuales/image010.jpg"/> <b >Nombre
de usuario</b>: en donde se visualiza el nombre del usuario que esta
autenticado actualmente.</p>

<p style="margin-left: 36pt; text-align: justify;" ><img height="23" width="23" v:shapes="Imagen_x0020_6" alt="Descripción: Descripción: Descripción: appearance" src="/images/manuales/image011.jpg"/> <b >Temas</b>:
un vínculo que nos permite cambiar el fondo de pantalla con imágenes de una
galería ya predefinida.</p>

<p style="margin-left: 36pt; text-align: justify;" ><img height="26" width="21" v:shapes="Imagen_x0020_7" alt="Descripción: Descripción: Descripción: candado" src="/images/manuales/image013.jpg"/> <b >Contraseña</b>:
el lugar en donde tendremos la posibilidad de cambiar nuestra contraseña
regularmente.</p>

<hr>
<a name="temas"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Temas</b></p>

<p style="text-align: justify;" >Motivado a siempre tener una vista
fresca del SIGLA cada uno de los usuarios podrá cambiar a su gusto el fondo del
escritorio con imágenes ya preestablecidas por la institución.</p>

<p style="text-align: justify;" >Al ingresar en Temas tendremos la
siguiente pantalla.</p>

<p align="center" style="text-align: center;" ><img width="400" src="/images/manuales/image015.jpg"/></p>

<p style="text-align: justify;" >En esta pantalla se muestran las
imágenes disponibles como fondos, la forma de establecer un fondo es
seleccionando el siguiente icono <img height="23" width="21" src="/images/manuales/image017.jpg"/> ubicado debajo de cada imagen al lado del
nombre de la misma. </p>

<hr>
<a name="contrasena"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Contraseña</b></p>

<p style="text-align: justify;" >Por motivos de seguridad es
recomendable cambiar la contraseña periódicamente a través de esta opción. La
pantalla se muestra de la siguiente manera:</p>



<p align="center" style="text-align: center;" ><img width="350" src="/images/manuales/image019.jpg"/></p>

<p >Escriba su contraseña actual, seguidamente ingrese la nueva contraseña
y repítala para verificar que la introdujo correctamente. La creación de una
nueva contraseña posee las siguientes restricciones que nos permiten tener un
mayor nivel de seguridad:</p>

<p style="margin-left: 36pt; text-indent: -18pt;" >•
No debe ser el número identificador del usuario
(cédula, RIF).</p>

<p style="margin-left: 36pt; text-indent: -18pt;" >•
No podrá repetir contraseñas anteriormente ingresadas.</p>

<p style="margin-left: 36pt; text-indent: -18pt;" >•      
No pueden ser el mismo nombre de usuario.</p>

<p style="margin-left: 36pt; text-indent: -18pt;" >•      
No pueden ser cadenas como <b >'123456'</b> o <b >'abcdef'</b>.</p>

<p style="margin-left: 36pt; text-indent: -18pt;" >•      
Debe tener al menos seis caracteres.</p>

<p style="text-align: justify; border: medium none; padding: 0cm;" > </p>

