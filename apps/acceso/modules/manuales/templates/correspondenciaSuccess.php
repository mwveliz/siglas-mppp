<h2><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a> Recibir una correspondencia interna</h2>

<p style="text-align: justify;">
Usted podrá recibir y visualizar todas las correspondencias de las oficinas a 
las que tenga permiso de grupo ingresando en la opción 
<img height="14" src="/images/manuales/image068.jpg"/>
del menú de herramientas, la cual nos mostrara el siguiente listado:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image070.jpg"/>
</p>
<p style="text-align: justify;">
Al igual que la bandeja de Enviadas cada fila en el listado representa 
una correspondencia, la cual se desglosa en las mismas cuatro columnas. 
Para recibir una correspondencia basta con hacer clic en el icono
<img height="14" src="/images/manuales/image072.png"/>
Leer, que inmediatamente nos descubrirá la información del documento, 
mostrara las acciones correspondientes y cambiara el estatus, quedando 
de la siguiente manera:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image074.jpg"/>
</p>

<p style="margin-left: 15pt; text-align: justify;" >• <b>Acciones</b>: al igual que la 
    bandeja de enviadas, existen acciones en la bandeja de recibidas:</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image059.png"/><b><i>Seguimiento:</i></b>
        muestra de forma gráfica el flujo de trabajo por el cual ha pasado la 
        correspondencia.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image076.png"/><b><i>Responder:</i></b>
        que permite generar una nueva correspondencia dependiente de la que 
        seleccionamos.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image078.png"/><b><i>Devolver:</i></b>
        opción disponible para devolver una correspondencia a su emisor con la 
        intención de que pueda modificarla o corregirla, esta opción está disponible 
        únicamente si no ha sido procesada o respondida.</p>



