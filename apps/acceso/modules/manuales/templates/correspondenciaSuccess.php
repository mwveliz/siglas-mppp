<p style="text-align: justify;"> <h2>Correspondencia</h2></p>

<p style="text-align: justify;" >El Sistema de Correspondencia es la herramienta 
principal del SIGLA, su objetivo principal es automatizar el registro, control y 
seguimiento la misma, a través de la creación de flujos de trabajo y la digitalización 
de las tipologías documentarias o formatos manejados en la institución, además de ello, 
haciendo uso de diferentes tecnologías que permiten el conocimiento de la información 
de manera rápida y oportuna.
<br/>
Las opciones para el manejo de la correspondencia se encuentran en la parte superior 
del menú de herramientas siendo las mismas 
<b>Enviada</b>, <b>Recibida</b>, <b>Externa</b> y <b>Grupos</b>.
<br/><br/>
Antes de explicar cómo enviar, recibir y procesar una correspondencia debemos saber 
lo siguiente:
<br/><br/>
Una correspondencia es el envío y recepción de un documento, la información 
de cada documento se clasifica de la siguiente manera:
<br/></p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Correlativo</b>: código secuencial con que se 
    identifica un documento, este código puede estar estructurado de forma 
    que permita identificar información limitada del mismo, como la unidad 
    que lo emite, año de emisión y tipo de documento entre otros.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Emisor</b>: persona que emite o firma el documento, 
    este no debe confundirse con la persona que lo redacta ya que puede ser una 
    distinta, Ejemplo: El director(a) y la secretaria.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Receptor</b>: persona o personas a la que está 
    dirigido, pudiendo ser internos (de la misma institución) o externos 
    (de otras instituciones).</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Contenido</b>: es la información que explica o 
    detalla las variables que permiten procesar el documento, como por ejemplo 
    los párrafos de un documento “<b>Memorándum</b>”, la fecha de salida de un documento 
    “<b>Solicitud de Vacaciones</b>”, el listado de artículos del documento 
    “<b>Solicitud de Materiales</b>” y muchos más según sea el caso.</p>

