<a name="enviar_correspondencia"></a>
<h2><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a> Enviar una correspondencia</h2>

<p style="text-align: justify;">
Para realizar esta acción debes cerciorarte de tener configurado correctamente 
el sistema para ser usado por la oficina o unidad a la que pertenecemos. 

Selecciona la opción <img height="14" src="/images/manuales/image033.jpg"/> del 
menú de herramientas, seguidamente a la opción 
<img height="14" src="/images/manuales/image029.jpg"/>
la cual mostrara la pantalla inicial de una correspondencia nueva:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image039.jpg"/>
</p>
<p style="text-align: justify;">
Esta pantalla inicialmente muestra los campos 
</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Documento</b>: muestran los 
    tipos de documento que puede emitir según la oficina o las oficinas a las que tiene 
    permiso como grupo, así como también los tipos de documentos de carácter personal. </p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Correlativo</b>: se refiere 
    al momento que desea generar el correlativo para el documento que está ingresando, 
    pudiendo seleccionar <i>“Al momento de guardar”</i> que generara el correlativo 
    inmediatamente registrado el documento y <i>“En otro momento”</i> para generar el 
    correlativo en un futuro cercano.</p>
<p style="text-align: justify;">
Una vez seleccionado el Documento que desea registrar, automáticamente se 
visualizaran los componentes <b>Emisor</b>, <b>Receptor</b> 
y <b>Contenido</b> los cuales variaran según el tipo de documento seleccionado.
</p>
<p style="text-align: justify;">
De manera general el componente <b>Emisor</b> se visualiza de la siguiente manera:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image041.jpg"/>
</p>
<p style="text-align: justify;">
y contiene una o más listas desplegables donde se visualizaran las 
personas que pueden firmar o emitir el documento.
</p>
<p style="text-align: justify;">
El componente <b>Receptor</b> se conforma de dos tipos <i>“Internos”</i> y <i>“Externos”</i>:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image043.jpg"/>
</p>
<p style="text-align: justify;">
Para los receptores internos, seleccionamos la unidad a la que queremos enviar 
y seguidamente la persona. En el caso de receptores externos escriba el nombre 
del Organismo al que envía, el nombre de la persona a la que está dirigido 
el documento y el cargo que asume dentro del organismo.
</p>
<p style="text-align: justify;">
<i><u>Nota:</u></i> los receptores internos y externos pueden variar dependiendo del 
documento que registra.
</p>
<p style="text-align: justify;">
El Componente <b>Contenido</b> es el de mayor variación, mostrando formularios totalmente 
diferentes por cada tipo documento que se registra, algunas pantallas son las siguientes:
</p>

<p style="text-align: center;">
<table align="center">
    <tr>
        <td width="90">Memorándum</td>
        <td width="390"><br/><img width="390" src="/images/manuales/image045.jpg"/></td>
    </tr>
    <tr>
        <td width="90">Solicitud de Servicios</td>
        <td width="390"><br/><img width="390" src="/images/manuales/image047.jpg"/></td>
    </tr>
    <tr>
        <td width="90">Solicitud de Materiales</td>
        <td width="390"><br/><img width="390" src="/images/manuales/image049.jpg"/></td>
    </tr>
    <tr>
        <td width="90">Solicitud de Vacaciones</td>
        <td width="390"><br/><img width="390" src="/images/manuales/image051.jpg"/></td>
    </tr>
</table>
</p>
<p style="text-align: justify;">
Para guardar el documento registre los datos de cada componente y presione el botón 
<i>“Guardar”</i>; una vez realizado se creara un registro en el listado de correspondencias 
enviadas.
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image053.jpg"/>
</p>
<p style="text-align: justify;">
Cada fila en el listado representa una correspondencia, la cual se desglosa en 
cuatro columnas que muestran la información del documento y las acciones que 
puede realizar con el mismo.
</p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Identificación</b>: 
    de arriba hacia abajo se muestra el correlativo identificador, el nombre 
    del emisor o firmante del documento y por último el o los receptores. </p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Documento</b>: aquí 
    es donde se visualizara el componente contenido del documento, dependiendo 
    del tipo de documento aparece el icono <img height="14" src="/images/manuales/image054.png"/> “Descargar”, al darle clic a este icono descargaremos 
    la versión para imprimir del documento.</p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Detalles</b>: de arriba 
    hacia abajo muestra el Estatus de la correspondencia:</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>En creación</i></b>: cuando 
        la correspondencia no ha sido firmada y enviada por el emisor.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Enviada</i></b>: cuando la 
        correspondencia ya está en la bandeja de recibidas del receptor pero sin 
        embargo no ha sido leída.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Recibida</i></b>: cuando el 
        receptor o uno de los receptores ya abrió la correspondencia a través del 
        sistema.</p>

<p style="margin-left: 15pt; text-align: justify;" >La fecha de envió, la 
    cantidad de archivos adjuntos y la cantidad de envíos físicos.</p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Acciones</b>: muestra los 
    iconos de las acciones que podemos realizar sobre la correspondencia, los datos 
    del usuario que creo el registro, la fecha y la hora de creación. Las acciones 
    aplicables son las siguientes:</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image055.png"/><b><i>Editar:</i></b>
        permite ingresar al documento en modo edición para modificar el contenido 
        del mismo.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image057.png"/><b><i>Firmar y enviar:</i></b>
        acción mostrada únicamente a los firmantes de la correspondencia, ejecuta el nivel 
        de aprobación del firmante, cambia el estatus a enviada y envía la información, 
        permitiendo que aparezca en la bandeja de recibidas del receptor.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image059.png"/><b><i>Seguimiento:</i></b>
        muestra de forma gráfica el flujo de trabajo por el cual ha pasado la 
        correspondencia.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image061.png"/><b><i>Anular:</i></b>
        permite anular el registro, liberando el correlativo para su posterior uso. 
        Esta opción estará disponible siempre y cuando no haya sido recibida.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image063.png"/><b><i>Pausar:</i></b>
        aparece una vez enviada la correspondencia y cuando no haya sido recibida, 
        esta opción permite pausar la correspondencia luego de ser enviada, es decir 
        extraerla de la bandeja de recibidas del receptor.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image065.png"/><b><i>Enviar:</i></b>
        aparece una vez enviada la correspondencia y cuando no haya sido recibida, 
        esta opción permite pausar la correspondencia luego de ser enviada, es decir 
        extraerla de la bandeja de recibidas del receptor.</p>
    <p style="margin-left: 30pt; text-align: justify;" >
        <img height="14" src="/images/manuales/image067.png"/><b><i>Quitar firma:</i></b>
        permite quitar la firma de una correspondencia pausada y así poder editarla.</p>
<hr>
<p style="text-align: justify;">
<i><u>Nota:</u></i> en la columna Identificación aparecerá un círculo animado de 
color azul identificando la última correspondencia en la que ejecutamos alguna acción.
</p>
    
    
    

