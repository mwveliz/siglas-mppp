<a name="externa_correspondencia"></a>
<h2><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a> Recibir una correspondencia externa</h2>


<p style="text-align: justify;">
Para recibir una correspondencia externa tiene que cerciorarse de que tiene 
el permiso <i>“Recibir Externa”</i> en el grupo. <br/><br/>
Selecciona la opción <img height="14" src="/images/manuales/image079.jpg"/>
del menú de herramientas, seguidamente a la opción
<img height="14" src="/images/manuales/image029.jpg"/>
, la pantalla de recepción externa se divide en los componentes:
</p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Identificación</b>:</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image081.jpg"/>
</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Nº de Entrada:</i></b> 
        correlativo de entrada, generado automáticamente.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Correlativo del Emisor:</i></b> 
        correlativo con que el organismo externo identifica el documento.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Fecha de Envío:</i></b> 
        fecha en la que el organismo externo envía la correspondencia.</p>
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Receptor</b>:</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image083.jpg"/>
</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Unidad:</i></b> 
        unidad a la que está dirigida la correspondencia.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Funcionario:</i></b> 
        persona a la que está dirigida la correspondencia.</p>
    
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Emisor</b>:</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image085.jpg"/>
</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Organismo:</i></b> 
        organismo externo que emite la correspondencia, en caso de ser 
        personas escriba <i>“Particular”</i>.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Persona:</i></b> 
        nombre de la persona que emite la correspondencia.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Cargo:</i></b> 
        cargo de la persona que emite la correspondencia.</p>
    
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Contenido</b>:</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image087.jpg"/>
</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Resumen:</i></b> 
        escriba un resumen del contenido del documento externo que registra.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Copia Digital:</i></b> 
        adjunte el archivo del documento digitalizado, el cual podrá ser 
        descargado en cualquier momento.</p>
    
<hr>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Forma de envío</b>:</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image089.jpg"/>
</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Tipo de traslado externo:</i></b> 
        seleccione la forma en que llego la correspondencia.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Empresa traslado:</i></b> 
        de seleccionar la opción <i>“Empresa de Encomiendas”</i> en el tipo de traslado, 
        seleccione la empresa.</p>
    <p style="margin-left: 30pt; text-align: justify;" >- <b><i>Nº de guía:</i></b> 
        escriba el número de guía del paquete.</p>
<hr>
<p style="text-align: justify;">
Para guardar la recepción externa registre los datos de cada componente y presione 
el botón <i>“Guardar”</i>; una vez realizado se creara un registro en el listado de 
correspondencias externas similar a la bandeja de recibidas.
</p>   


