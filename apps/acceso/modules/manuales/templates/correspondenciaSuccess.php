<a name="configurar_correspondencia"></a>
<h2><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a> Configurar el sistema para ser usado por la unidad u oficina</h2>

<p style="text-align: justify;" >
  Esta acción únicamente puede ser realizada por los funcionarios de mayor 
  jerarquía de cada unidad, la configuración se divide en dos partes: crear el grupo de 
  correspondencia y crear los correlativos de la unidad.  
</p>
<hr>
<a name="grupos"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Grupo de Correspondencia</b>:
en él se identifican las personas y los permisos de acceso a la correspondencia 
de la unidad. Existen cinco permisos disponibles para cada persona:
</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Redactar</b>: permite redactar 
    documentos específicos de la unidad al mismo tiempo que hacer uso de los correlativos 
    de la misma.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Leer</b>: da acceso a visualizar 
    la información de todos los documentos que recibe la unidad.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Recibir Externa</b>: permite al 
    funcionario registrar o dar entrada a documentos de origen externo.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Firmar</b>: autoriza al 
    funcionario que tenga este permiso emitir o firmar los documentos de la unidad.</p>
<p style="margin-left: 15pt; text-align: justify;" >• <b>Administrar</b>: da la opción al 
    funcionario de agregar, modificar y eliminar personas al grupo de correspondencia.</p>

<p style="text-align: justify;" >
Para configurar el grupo debemos ingresar a la opción <img height="14" src="/images/manuales/image025.jpg"/> 
en el menú de herramientas (opción disponible para funcionarios de alta jerarquía), 
mostrando el listado de las personas ya agregadas al grupo o la frase de “Sin resultados” 
si no ha sido agregada ninguna. 
</p>

<p style="text-align: justify;" >
Para agregar una persona y sus permisos hacemos clic en el enlace <img height="14" src="/images/manuales/image029.jpg"/> el cual nos 
muestra la siguiente pantalla.
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image031.jpg"/>
</p>
<p style="text-align: justify;" >
Para seleccionar la persona que deseamos agregar al grupo seleccione la unidad 
a la que pertenece del listado “Unidad”, seguidamente seleccione la persona del 
listado “Funcionario”, marque los permisos deseados y presionamos el botón “Guardar”.
</p>

<hr>
<a name="correlativos"></a>
<p style="text-align: justify;"><a href="#inicio" title="Subir"><img src="/images/manuales/subir.png"/></a>&nbsp;<b >Correlativos</b>:
La estructura de un correlativo principalmente se compone de las siglas de la 
unidad, el año o mes o día en que se emite el documento, una letra que identifica 
el correlativo en caso de crear más de uno y una secuencia que incrementara 
automáticamente, todos estos componentes combinados a través de un “Nomenclador”. 
Un correlativo puede ser asignado a uno o varios tipos de documentos.
</p>
<p style="text-align: justify;">
Los correlativos que identifican los documentos de la unidad se crean ingresando 
a la opción <img height="14" src="/images/manuales/image033.jpg"/> del menú de 
herramientas, seguidamente a la opción <img height="14" src="/images/manuales/image035.jpg"/> 
y luego <img height="14" src="/images/manuales/image029.jpg"/> mostrando 
la siguiente pantalla:
</p>
<p style="text-align: center;" >
<img width="490" src="/images/manuales/image037.jpg"/>
</p>
<p style="text-align: justify;">
Para definir y guardar el correlativo ingrese una Letra en caso de ser necesario, 
seleccione un nomenclador, ingrese un numero con el cual comenzara a funcionar el 
correlativo y seleccione los tipos de documentos asociados a este, una vez llenado 
el formulario presione el botón Guardar.
</p>
<p style="text-align: justify;">
<i><u>Nota:</u></i> al seleccionar un nomenclador con Año, la secuencia se reiniciara anualmente, 
al seleccionar con Año y Mes se reiniciara mensualmente y al seleccionar con Año, 
Mes y Día se reiniciara diariamente.
</p>


